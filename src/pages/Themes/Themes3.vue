Для этого любой документ, помещаемый в базу данных или знаний, должен иметь имя (условное обозначение). Оно может присваиваться до­кументу путем его <i>индексирования</i> – методики описания данных в базе с помощью указателей (индексов). Файл, обращение к содержимому которо­го происходит при помощи индексов, называют индексированным. Метод доступа к такому файлу именуется индексно-последовательным.<br />Процесс индексирования заключается в нахождении набора слов или кодов, служащих для идентификации документов и используемых для их поиска. Существует несколько методов индексирования. Статистиче­ский метод заключается в том, что из текста документа отбрасываются все незначащие слова, например: <i>из, под, но.</i> Затем подсчитывается час­тота употребления каждого оставшегося слова <b>и учитывается отклонение частоты слов в документе от ожидаемой. В результате, создается <i>поиско­вый образ документа</i>.</b> Он готовится специалистом либо прикладной про­граммой и от его качества зависит полнота поиска, т.е. процент найденных документов, содержащих искомые сведения. Используются три степени поиска: по заголовкам, рефератам, полным текстам.<br />Документооборот определяет потоки электронных документов, не­обходимых в организации либо на предприятии для обеспечения эффек­тивной координации и управления работой. Осуществляется документо­оборот с помощью системы, состоящей из комплекса прикладных про­грамм. Последние обеспечивают запуск процессов, поиск документов, их анализ, классификацию, хранение и передачу в заданные информационные системы.<br />Для функционирования системы автоматизируемый процесс должен быть выделен из всей массы выполняемых на предприятии работ. Естест­венно, что этот процесс должен быть формализован и описан в виде алго­ритма. В выделенном бизнес-процессе участвует группа сотрудников предприятия (организации). Поэтому, в нужных случаях, должны быть уч­тены вопросы синхронизации их деятельности.<br />

