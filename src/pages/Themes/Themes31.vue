<b>Создание гиперссылок в тексте</b><br />
Гиперссылку можно создать в текстовом документе MS Word несколькими способами.<br />
<img src="img/img-4.png"><br />
 Основные из них связаны с диалоговым окном <b>Добавление гиперссылки</b>.<br />
 Это диалоговое окно открывается командой <b>Вставка – Гиперссылка</b>. В верхней части диалогового окна располагаются элементы управления, позволяющие задать текст, щелчок на котором будет восприниматься как команда перехода по гиперссылке, и текст подсказки, всплывающей при наведении указателя мыши на текст гиперссылки. Если в момент открытия диалогового окна в текстовом документе будет выделен фрагмент текста, то он становится текстом гиперссылки. Если в момент открытия 
диалогового окна будет выделен графический элемент текстового документа, гиперссылка будет связана с ним. В последнем случае текст гиперссылки не определяется.<br />
Текст подсказки гиперссылки (текстовой или графической) вводится в единственное текстовое поле вспомогательного диалогового окна, раскрывающегося кнопкой <b>Подсказка.</b><br />
Остальные элементы управления диалогового окна <b>Добавление гиперссылки</b> предназначены для задания адреса гиперссылки. В левой части окна представлены несколько вариантов задания адреса гиперссылки.<br />
<b>Вставка гиперссылки на элемент того же текстового документа</b><br />
Вставка в документ Web гиперссылок на элементы, расположенные в том же документе, дает замечательную возможность автоматизировать перемещение по документу.<br />
Чтобы вставить в документ MS Word гипертекстовую ссылку на элемент того же документа, необходимо выполнить следующие шаги:<br />
<ol>
	<li>Если элемент, на который должна привести гиперссылка, не является заголовком оглавления документа, и на него не была ранее определена закладка, определить описанным выше приемом закладку с уникальным именем.</li>
	<li>Выбрать в тексте документа фрагмент, который будет служить текстом гиперссылки, или графический элемент, с которым будет связана гиперссылка.</li>
	<li>Открыть диалоговое окно <b>Добавление гиперссылки </b>командой <b>Вставка - Гиперссылка.</b></li>
	<li>При необходимости задать текст всплывающей подсказки, щелкнуть мышью на кнопке <b>Подсказка </b>и ввести текст в поле вспомогательного диалогового окна, а затем щелкнуть в нем кнопку <b>OK</b><b>.</b></li>
	<li>В зоне выбора места в документе диалогового окна <b>Добавление гиперссылки </b>выбрать щелчком мыши заголовок или закладку, определяющую адрес формируемой гиперссылки, а затем щелкнуть в нем кнопку <b>OK</b>.</li>
</ol>
<b>Чтобы вставить в документ </b><b>MS</b><b> </b><b>Word</b><b> гиперссылку на другой документ, следует:</b><br />
<ol>
	<li><b>Открыть</b> файл, на который будет ссылаться новая гиперссылка, и, при необходимости, вставить новую закладку.</li>
	<li><b>Открыть</b> файл, в который должна быть вставлена новая гиперссылка, и выбрать текст или объект, который будет связан с ней.</li>
	<li><b>Открыть</b> диалоговое окно <b>Добавление гиперссылки </b>командой <b>Вставка – Гиперссылка.</b></li>
	<li>В списке <b>Связать</b> c диалогового окно <b>Добавление гиперссылки </b>выбрать вариант 1 имеющимся файлом, <b>Web</b><b>-страницей</b>.</li>
	<li>Найти и выбрать документ, на который должна указывать гиперссылка, воспользовавшись кнопкой <b>Файл.</b></li>
	<li>При необходимости указать в гиперссылке закладку, нажав кнопку <b>Закладка</b>, а затем выбрать нужную.</li>
	<li>При необходимости задать текст всплывающей подсказки, щелкнуть мышью на кнопке Подсказка и ввести текст подсказки в поле вспомогательного диалогового окна, а затем нажать в нем кнопку <b>OK</b><b>.</b></li>
	<li>Создать гиперссылку, нажав кнопку <b>OK</b> диалогового окна <b>Добавление гиперссылки.</b></li>
</ol>
<b>Вставка оглавлений</b><br />
В текстовом процессоре <b>MS Word</b> предусмотрена значительная степень автоматизации многих действий по оформлению отдельных структурных элементов документа. Это вставка сносок, колонтитулов, создание оглавлений (или содержаний) и предметных указателей.<br />
Для того чтобы оглавление могло быть вставлено автоматически, при наборе или форматировании документа все заголовки определенного уровня должны быть оформлены с помощью соответствующего стиля заголовка (<b>ЗАГОЛОВОК1, ЗАГОЛОВОК2</b> и т.д.). Всего в иерархии заголовков возможно девять уровней.<br />
Затем в соответствующем месте документа <b>(если это оглавление, то в начале, если содержание — в конце)</b> осуществляется вставка оглавления.<br />
 Это можно проделать одним из способов: меню <b>Вставка ==&gt; Ссылка ==&gt; Оглавление и указатели</b> или через панель инструментов <b>Структура</b>, инструмент <b>Оглавление</b><br />          
 <img src="img/img-5.png"><br />
 <img src="img/img-6.png"><br />
 В диалоговом окне необходимо выбрать формат оглавления (т.е. один из вариантов оформления), ориентируясь на образец; заполнитель между последним словом пункта оглавления и номером страницы; задать, если необходимо, количество уровней заголовков в структуре документа, по которым собирается оглавление.<br />
После этого оглавление можно вставить. Если что-либо в его оформлении не устраивает, вставку можно повторить. Довольно часто по ошибке стилем заголовков оформляют не являющиеся таковыми абзацы. Естественно, что они попадают в оглавление. Необходимо очистить у них формат заголовка, задав нужный стиль, и повторить вставку оглавления или произвести обновление оглавления: панель инструментов <b>Структура</b>, кнопка <b>Обновить оглавление</b> .<br />
<b>Создание указателей и сносок</b><br />
При работе с большими документами, например, техническими, достаточно большую помощь в поиске нужной информации может оказать предметный указатель. Как известно, чаще всего предметный указатель содержит перечень терминов и страницы, на которых они упоминаются.<br />
Создание предметного указателя происходит в два этапа.<br />
На первом этапе должны быть помечены один за другим все термины и другие элементы текста, которые предполагается включить в указатель. Делается это следующим образом. Прежде всего, термин выделяется в тексте. Затем обращаемся к меню <b>Вставка ==&gt; Ссылка ==&gt; Оглавление и указатели</b>, вкладка <b>Указатель</b>.<br />
<img src="img/img-7.png"><br />
В этом окне нажимается кнопка <b>Пометить</b>, что открывает ещё одно окно <b>Определение элемента указателя</b>.<br />
<img src="img/img-8.png"><br />
Здесь задаются основной и дополнительный (если необходимо) элементы указателя, для правильной ссылки должен быть установлен переключатель <b>текущая страница</b>, можно указать формат номера страницы. Затем выбирается кнопка <b>Пометить</b> (кнопка <b>Пометить все </b>может быть выбрана в том случае, когда во всем документе нужно отметить текст, заданный как основной).<br />
Теперь можно, не закрывая окно <b>Определение элемента указателя</b>, перейти к следующему элементу.<br />
Второй этап заключается собственно во вставке предметного указателя. Для этого курсор устанавливается в то место документа, где должен быть указатель, и через меню <b>Вставка ==&gt; Ссылка ==&gt; Оглавление и указатели</b>, вкладка <b>Указатель</b>, выбрав кнопку <b>ОК</b>, вставляем указатель.<br />
Аналогично оглавлению предметный указатель может быть изменен и обновлен. В документе <b>MS Word</b> можно использовать два вида сносок — в конце страницы и в конце документа.<br />
Для добавления сноски используется меню <b>Вставка ==&gt; Ссылка ==&gt; Сноска</b>, после чего в диалоговом окне задаются все параметры сноски: положение сноски — внизу страницы или в конце документа, формат номера и область применения заданных изменений.<br />
<img src="img/img-9.png"><br />
После нажатия кнопки Вставить набирается текст сноски.<br />
<b>Колонтитулы</b><br />
На каждой странице документа (вверху или внизу) может размещаться повторяющийся текст – колонтитул.<br />
Для вставки нового колонтитула выбирают меню <b>Вид ==&gt; Колонтитулы</b>. При этом основной текст становится недоступным для редактирования, можно работать лишь с колонтитулом. Кроме того, открывается панель инструментов <b>Колонтитулы</b>. Текст колонтитула вводится, редактируется и форматируется точно так же, как и основной документ. С помощью панели инструментов <b>Колонтитулы</b> можно перемещаться между верхним и нижним колонтитулом.<br />
Щелчок по кнопке <b>Закрыть</b> на панели инструментов Колонтитулы завершает работу с колонтитулом. Для редактирования колонтитула достаточно сделать двойной щелчок в его области. Удалить колонтитул можно, удалив его содержание.<br />
