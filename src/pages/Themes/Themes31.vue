<b><u>1 урок — как пронумеровать страницы</u></b>
Это самая распространенная задача в Word. Используется практически для всех документов: будь то у вас диплом, курсовая, или просто вы печатаете документ для себя. Ведь если не задать номера страниц, то при распечатке документа все листы можно хаотично перепутать…<br />
Хорошо если у вас 5-10 страничек, которые можно логически разложить по порядку за несколько минут, а если их 50-100 или более?!<br />
<u>Чтобы вставить номера страниц в документ</u> — перейдите в раздел «<u>Вставка</u>«, затем в открывшемся меню найдите раздел «<u>Колонтитулы</u>«. В нем будет выпадающее меню с функции нумерации страниц (см. рис. 1).<br />
<img src="img/img-65.png" ><br />
Довольно распространена задача <u>нумерации страниц кроме первой</u> (или первых двух). Это актуально, когда на первой страничке титульный лист или содержание.<br />
Делается это довольно просто. Щелкаете двойным щелчком по самому номеру первой страницы: в верхней панели Word появиться дополнительное меню «<u>Работа с колонтитулами</u>«. Далее заходим в это меню и ставим галочку напротив пункта «<u>Особый колонтитул на первой странице</u>«. Собственно, на этом все — у вас нумерация пойдет со второй страницы (см. рис. 2).<br />
<i>Добавление</i>: если вам нужно поставить нумерацию с третьей страницы — то используйте в инструмент «Макет/вставка разрыва страницы»<br />
<img src="img/img-66.png" ><br />
<b><u>2 урок — как в Word сделать линию</u></b><br />
Когда спрашивают о линиях в Word, так сразу и не поймешь, что имеют ввиду. Поэтому я рассмотрю несколько вариантов, чтобы точно попасть в «цель». И так…<br />
Если вам нужно просто подчеркнуть линией какой-нибудь слово, — то в разделе «Главная» есть специальная функция для этого — «<u>Подчеркивание</u>» или просто буква «<u>Ч</u>«. Достаточно выделить текст или слово, а затем нажать на эту функцию — текст станет подчеркнутым линией (см. рис. 3).<br />
<img src="img/img-67.png" ><br />
Если же вам нужно вставить просто линию (неважно какую: горизонтальную, вертикальную, наискосок и пр.), то перейдите в раздел «<u>Вставка</u>» и выберите вкладку «<u>Фигуры</u>«. Среди разнообразных фигур есть и линия (вторая по списку, см. рис. 4).<br />
<img src="img/img-68.png" ><br />
Ну и наконец еще один способ: просто зажать на клавиатуре клавишу тире «-» (рядом с «Backspace»).<br />
<b><u>Урок 3 — как сделать красную строку</u></b><br />
В некоторых случаях необходимо оформить документ с конкретными требованиями (например, пишете курсовую и преподаватель четко оговорил, как она должна быть оформлена). Как правило, в этих случаях требуется выполнить красную строку для каждого абзаца в тексте. У многих пользователей возникает дилемма: как ее сделать, да и еще сделать точно нужного размера.<br />
Рассмотрим вопрос. Сначала необходимо включить инструмент «<u>Линейка</u>» (по умолчанию он в Word выключен). Для этого перейдите в меню «<u>Вид</u>» и выберите соответствующий инструмент (см. рис. 5).<br />
<img src="img/img-69.png" alt=""><br />
Далее поставьте курсор перед первой буквой в первом предложении любого абзаца. Затем на линейке потяните <u>верхний</u> индикатор в правую сторону: вы увидите, как появиться красная строка (см. рис. 6. Кстати, многие ошибаются и двигают оба ползунка, из-за этого у них не получается). Благодаря линейке, красную строку можно отрегулировать очень точно в нужный размер.<br />
<img src="img/img-70.png" ><br />
Дальнейший абзацы, когда вы будете нажимать клавишу «Enter» — будут получаться автоматически с красной строкой.<br />
<b><u>4 урок — как создать оглавление (или содержание)</u></b><br />
Оглавление — это довольно трудоемкая задача (если делать ее неправильно). А многие начинающие пользователи сами составляют лист с содержанием всех глав, проставляют страницы и т.д. А в Word есть специальная функция для авто-создания оглавления с авто-постановкой всех страниц. Делается это очень быстро!<br />
Сначала в Word необходимо выделить заголовки. Делается это очень просто: листаете свой текст, встречаете заголовок — выделите его курсором, затем в разделе «<u>Главная</u>» выберите функцию выделения заголовка (см. рис. 7. Кстати, обратите внимание, что заголовки могут быть разными: заголовок 1, заголовок 2 и т.д. Они отличаются между собой по старшинству: т.е. заголовок 2 будет входить в раздел вашей статьи помеченный заголовком 1).<br />
<img src="img/img-71.png" ><br />
Теперь для создания оглавления (содержания) достаточно перейти в раздел «<u>Ссылки</u>» и выбрать меню оглавление. В месте курсора появиться оглавление, в котором страницы на нужные подзаголовки (которые мы отметили до этого) будут проставлены автоматически!<br />
<img src="img/img-72.png" ><br />
<b><u>5 урок — как «рисовать» в Word (вставка фигурок)</u></b><br />
Добавлять различные фигурки в Word бывает очень полезно. Помогает более наглядно показать на что обратить внимание, легче воспринимать информацию читающему ваш документ.<br />
Для вставки фигурки, перейдите в меню «<u>Вставка</u>» и во вкладке «<u>Фигуры</u>» выберите нужный вариант.<br />
<img src="img/img-73.png" alt=""><br />
Кстати, комбинации из фигурок при небольшой сноровке могут дать самые неожиданные результаты. Например, можно что-нибудь нарисовать: диаграмму, рисунок и пр. (см. рис. 10).<br />
<img src="img/img-74.png" ><br />
<b><u>6 урок — удаление страницы</u></b><br />
Казалось бы, простая операция иногда может стать настоящей проблемой. Обычно, чтобы удалить страницу, достаточно воспользоваться клавишами Delete и Backspace. Но бывает так, что они не помогают…<br />
Дело здесь в том, что на странице могут быть «не видимые» элементы, которые не удаляются обычным способом (например, разрывы страниц). Чтобы их увидеть, перейдите в раздел «<u>Главная</u>» и нажмите кнопку отображения непечатаемых символов (см. рис. 11). После этого выделяете эти спец. символы и спокойно удаляете — в итоге страница удалена.<br />
<img src="img/img-75.png" ><br />
<b><u>7 урок — создание рамки</u></b><br />
Рамка бывает нужна в отдельных случаях, когда необходимо что-то выделить, обозначить или подытожить информацию на каком-то листе. Делается это достаточно просто: переходите в раздел «<u>Дизайн</u>«, затем выбираете функцию «<u>Границы страницы</u>» (см. рис. 12).<br />
<img src="img/img-76.png" ><br />
Затем нужно выбрать тип рамки: с тенью, двойная рамка и пр. Тут уж все зависит от вашей фантазии (либо требований заказчика документа).<br />
<img src="img/img-77.png" ><br />
<b><u>8 урок — как делать сноски в Word</u></b><br />
А вот сноски (в отличие от рамок) очень часто встречаются. Например, употребили вы редкое слово — хорошо бы дать на него сноску и в конце страницы его расшифровать (тоже относится и к словам имеющий двойной смысл).<br />
Чтобы сделать сноску, установите курсор на нужное место, затем перейдите в раздел «<u>Ссылки</u>» и нажмите кнопку «<u>Вставить сноску</u>«. После этого вас «перекинет» в конец страницы, чтобы вы могли написать текст сноски (см. рис. 14).<br />
