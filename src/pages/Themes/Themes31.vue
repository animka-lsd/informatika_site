<b><u>10 урок — как сделать альбомный лист</u></b><br />
По умолчанию в Word все листы имеют книжную ориентацию. Бывает так, что часто требуется альбомный лист (это когда лист находиться перед вами не вертикально, а горизонтально).<br />
Делается это достаточно просто: переходите в раздел «<u>Макет</u>«, затем открываете вкладку «<u>Ориентация</u>» и выбирайте нужный вам вариант (см. рис. 16). Кстати, если вам нужно поменять ориентацию не всех листов в документе, а только одного из них — используйте <i>разрывы</i> («<u>Макет/разрывы/разрывы страниц</u>«).<br />
