<b>Титульный лист</b><br />
Достаточно часто для оформления объемной работы используют титульный лист — первую страницу, на которой указывают название работы, автора и другие необходимые сведения.<br />
Для вставки в документ титульного листа перейдите на вкладку <b>Вставка</b> и в группе <b>Страницы</b> щелкните на кнопке <b>Титульная страница</b>. Появится меню, содержащее доступные шаблоны титульных страниц. Выберите из списка нужный вариант, в результате чего в документе появится первая страница указанного вида. Введите в поля нужный текст. Имя автора появится автоматически (<b>Word</b> вставит имя, указанное при установке программы), но при необходимости вы можете изменить это имя. Для этого просто щелкните на нем и наберите новое. Ненужные элементы можно удалить. Для этого щелкните на них и два раза нажмите клавишу <b>Delete.</b> Чтобы удалить титульную страницу, выполните команду Удалить титульную страницу в меню кнопки <b>Титульная страница.</b><br />
<b>Нумерация страниц</b><br />
Чтобы пронумеровать страницы, сделайте следующее.<br />
<ol>
	<li>Перейдите на вкладку <b>Вставка ленты</b> и в группе <b>Колонтитулы</b> щелкните на кнопке <b>Номер страницы.</b></li>
