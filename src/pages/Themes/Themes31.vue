<b><u>9 урок — как написать римские цифры</u></b><br />
Римские цифры обычно нужны для обозначения веков (т.е. чаще всего тем, кто связан с историей). Писать римские цифры очень просто: достаточно перейти на английский язык и ввести, скажем «XXX».<br />
