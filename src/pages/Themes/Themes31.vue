<b>Файл – Сохранить как</b> – выбрать нужную папку и сохранить работу.<br />
Кроме того, необходимо выбрать формат файла, который определяет способ хранения текста в файле. Существуют универсальные форматы текстовых файлов, которые могут быть прочитаны большинством текстовых редакторов, и оригинальные форматы, которые используются только определенными текстовыми редакторами<br />
Формат <b>ТХТ</b> (только текст, расширение в имени файла txt) является наиболее универсальным текстовым форматом. Файлы, сохраненные в этом формате, могут быть прочитаны приложениями, работающими в различных операционных системах. Достоинством этого формата является небольшой информационный объем файлов, а недостатком то, что не сохраняются результаты форматирования текста.<br />
Формат <b>RTF </b>(расширенный текстовый формат, расширение в имени файла rtf) является также универсальным форматом текстовых файлов, в котором сохраняются результаты форматирования. Недостатком этого формата является большой информационный объем файлов.<br />
