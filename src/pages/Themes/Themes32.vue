                <strong><strong class="name">Общие сведения</strong><br />
<b>Окно программы</b><br /><b>Microsoft Excel – </b>табличный процессор, программа для создания и обработки электронных таблиц. Microsoft Excel позволяет работать с таблицами в двух режимах:<br />· <b>Обычный</b> – наиболее удобный для выполнения большинства операций.<br />· <b>Разметка</b><b> </b><b>страниц</b><b> – </b>удобен для окончательного форматирования таблицы перед распечаткой. Границы между страницами в этом режиме отображаются синими пунктирными линиями. Границы таблицы – сплошной синей линией, перетягивая которую, можно изменять размеры таблицы.<br />Для перехода между режимами <b>Обычный</b> и <b>Разметка</b><b> </b><b>страниц</b><b> </b>используются соответствующие пункты меню <b>Вид</b>.<br />Под панелями инструментов Microsoft Excel обычно находится строка формул, а в нижней части окна – строка состояния. Чтобы вывести или убрать эти строки, следует в меню <b>Вид</b> выбрать соответствующие пункты: <b>Строка</b> <b>формул</b> или <b>Строка</b> <b>состояния</b>.<br />
