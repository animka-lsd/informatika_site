· в поле <b>Образец</b> будет отображаться пример содержимого ячейки в выбранном формате;<br />
· чтобы ввести новый формат, следует выбрать пункт <b>все форматы</b>, а затем в поле <b>Тип</b> ввести новый формат;<br />
· щелкнуть <b>ОК</b>.<br />
<b>Выравнивание содержимого ячеек</b><br />
Содержимое ячеек может быть выровнено по левому краю, по правому краю или по центру. На новом рабочем листе все ячейки имеют формат <b>Обычный</b>, в котором числа, даты и время выравниваются по правому краю ячейки, текст – по левому, а логические значения <b>ИСТИНА</b> и <b>ЛОЖЬ</b> – центрируются. Изменение выравнивания не влияет на тип данных. Для выравнивания содержимого ячеек необходимо:<br />
· выделить ячейки, которые следует отформатировать;<br />
· в меню<b> </b><b>Формат</b> выбрать команду <b>Ячейки</b><b>;</b><br />
· выбрать вкладыш <b>Выравнивание</b> (рис.10);<br />
· в поле списка <b>по горизонтали</b> выбирается тип выравнивания по горизонтали:<br />
<b>по значению</b> – выравнивание по умолчанию в зависимости от типа данных;<br />
<b>по левому краю (отступ)</b> – содержимое ячейки выравнивается по левому краю с отступом, указанным в поле справа;<br />
<b>по центру</b> – содержимое ячейки центрируется;<br />
<b>по правому краю</b> – содержимое ячейки выравнивается по правому краю;<br />
<b>с заполнением </b>– содержимое выделенного диапазона заполняется символами, указанными в левой ячейке выбранного диапазона;<br />
<b>по ширине</b> – содержимое ячейки разбивается на несколько строк, а пробелы между словами устанавливаются так, чтобы ширина строк соответствовала ширине ячейки;<br />
<b>по центру выделения</b> – содержимое левой ячейки выделенного диапазона выравнивается по центру диапазона (все другие ячейки выделенного диапазона должны быть пустыми);<br />
· в поле списка <b>по вертикали</b> выбирается тип выравнивания по вертикали:<br />
<b>по верхнему краю</b> – содержимое ячейки выравнивается по верхнему краю;<br />
<b>по центру</b> – по центру;<br />
<b>по нижнему краю</b> – по нижнему краю;<br />
<b>по высоте</b> – содержимое ячейки разбивается на несколько строк, первая и последняя строки выравниваются по верхней и нижней границе ячейки;<br />
<img src="img/img-87.png" ><br />
· в группе флажков <b>Отображение</b> можно включить следующие режимы:<br />
<b>переносить по словам</b> – по достижению правой границы ячейки текст будет переноситься на новую строку;<br />
<b>автоподбор ширины</b> – размер символов уменьшается так, что содержимое ячейки помещается в границах ячейки;<br />
<b>объединение ячеек</b> – выделенные ячейки объединяются в одну;<br />
· в рамке <b>Ориентация</b> выбирается направление расположения текста в ячейке – текст можно расположить вертикально или под углом.<br />
Чтобы выровнять текст по центру нескольких столбцов, необходимо:<br />
· выделить ячейку, содержащую данные, которые необходимо выровнять по центру нескольких столбцов, и пустые ячейки, находящиеся справа;<br />
<b>Установление шрифта </b><br />
Для установления шрифта необходимо:<br />
· выделить группу ячеек;<br />
· в меню <b>Формат</b> выбрать команду <b>Ячейки</b><b>;</b><br />
· выбрать вкладыш <b>Шрифт</b><b>;</b><br />
· в списке <b>Шрифт</b> выбирается тип шрифта;<br />
