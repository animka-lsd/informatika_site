По умолчанию ячейки имеют стандартную ширину и высоту. Высота строки определяется размером шрифта. Для изменения высоты строки или ширины столбца можно перетянуть границу заголовка до необходимого значения (на границе заголовка указатель мыши примет вид двунаправленной стрелки) (рис.11). Для изменения размеров сразу нескольких столбцов или строк следует их выделить и перетянуть границу заголовка одного из выделенных элементов. Если на границе заголовков столбцов дважды щелкнуть мышью, то ширина столбца установится по ширине ячейки с самым длинным содержимым.<br />
