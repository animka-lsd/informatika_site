<b>Форматирование ячеек</b><br />
<b> </b><br />
Для форматирования информации в таблице используются необходимые команды меню <b>Формат </b> или кнопки на панели инструментов.<br />
При вводе информации Exel автоматически выравнивает текст по левой границе, а цифры – по правой. Для изменения расположения информации в ячейке служат кнопки выравнивания, с помощью которых можно выровнять информацию по левой, по правой границе, по центру или ширине ячейки.<br />
Кроме того, Exel предоставляет новую возможность организации текста в ячейках. Можно выровнять текст по центру не только внутри колонки, но внутри помеченного блока колонок. Для этого надо выделить блок ячеек и нажать кнопку <b>Объединить и поместить в центре</b> на панели инструментов.<br />
В дополнение к этим средствам форматирования на панели инструментов помещены кнопки <b>Границы </b>и<b> Цвет заливки</b>, с помощью которых можно установить обрамление для каждой из ячеек, а также цвет фона.<br />
По умолчанию все ячейки имеют один и тот же размер, который в случае необходимости можно изменять. Для изменение ширины колонки надо стать на границу заголовка колонки, которую надо изменить, и, нажав левую клавишу мыши, установить требуемый размер. Аналогично можно изменить и высоту строки.<br />
Кроме этого, можно установить размер колонки или строки<b> автоматически</b>. Для этого достаточно дважды щелкнуть мышью по правой границе колонки или нижней границе строки. В этом случае будет установлен размер ячеек исходя из максимального размера записанной информации.<br />
<b>Вставка и удаление ячеек</b><br />
Чтобы вставить ячейку, надо выделить ячейку, перед которой ее надо вставить, и выбрать из меню <b>Вставка </b>команду <b>Ячейки.</b><br />
Если нужно добавить столбец ячеек, перед которым должен быть вставлен пустой, и выберите из меню <b>Вставка </b>команду <b>Столбец.</b> Точно так же можно вставить несколько столбцов или строк.<br />
Для удаления всей строки или столбца надо, выделив их, выбрать из меню <b>Правка</b> команду. У<b>далить </b>или воспользоваться контекстным меню.<br />
