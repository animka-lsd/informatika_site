Для точного установления ширины столбцов необходимо:<br />
· выделить столбцы;<br />
· выбрать в меню <b>Формат</b> пункт <b>Столбец</b>, затем пункт <b>Ширина</b><b>;</b><br />
· ввести в поле <b>Ширина столбца</b> значение ширины столбца (число символов, которые могут поместиться в столбце при использовании стандартного шрифта);<br />
· щелкнуть кнопку <b>ОК</b><b>.</b><br />
Команда <b>Автоподбор ширины</b> устанавливает ширину столбца по ширине ячейки с самым длинным содержимым. Команда <b>Стандартная ширина</b> предлагает изменить стандартную ширину для столбцов рабочего листа.<br />
Для точного установления высоты строк необходимо:<br />
· выделить строку или несколько строк;<br />
· выбрать в меню <b>Формат</b> пункт <b>Строка</b><b>,</b> а затем пункт <b>Высота</b><b>;</b><br />
· в поле <b>Высота строки</b> ввести значение высоты строки в пунктах;<br />
· щелкнуть <b>ОК</b><b>.</b><br />
Чтобы спрятать строки или столбцы необходимо:<br />
· выделить строки или столбцы, которые следует спрятать;<br />
· выбрать в меню <b>Формат</b> команду<b> </b><b>Строка</b> или команду <b>Столбец</b> соответственно, затем выбрать команду <b>Скрыть</b><b>.</b><br />
Чтобы спрятать строку, можно перетянуть нижнюю границу заголовка строки за верхнюю границу. Чтобы спрятать столбец, можно перетянуть правую границу заголовка столбца за левую. Утолщенная рамка строки или столбца, а также пропущенная буква столбца или пропущенный номер строки указывают на наличие скрытых строк или столбцов.<br />
Чтобы показать строки или столбцы необходимо:<br />
· выделить строки или столбцы с обеих сторон скрытой строки или столбца;<br />
