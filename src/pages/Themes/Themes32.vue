<b>Основные сведения</b><br />
Вычисления в таблицах выполняются с помощью <b>формул</b>. <b>Формула</b> может состоять из математических операторов, значений, ссылок на ячейку и имена функций. Результатом выполнения формулы есть некоторое новое значение, содержащееся в ячейке, где находится формула. Формула начинается со знака равенства &quot;<b>=</b>&quot;. В формуле могут использоваться арифметические операторы <b>+, -, *, /.</b> Порядок вычислений определяется обычными математическими законами.<br />
Примеры формул: <b>=(А4+В8)*С6, =F7*С14+B12.</b><br />
<b>Константы</b> – текстовые или числовые значения, которые вводятся в ячейку и не могут изменяться во время вычислений.<br />
<b>Все ссылки на ячейку или группу ячеек</b> делятся на относительные, абсолютные и смешанные. Ссылки :<br />
<b>–</b> по умолчанию для указания адресов ячеек применяются <b>относительные</b> ссылки, которые, при копировании формул автоматически изменяются на размер перемещения.<br />
(например: <b>F7</b><b>).</b><br />
<b>- </b>точные координаты<b> я</b>чейки обозначаются <b>абсолютными</b> ссылками, которые записываются в сочетании со знаком <b>$,</b><b> </b>при копировании формул адрес не изменяется<b>(</b>например<b>: </b><b>$F$7</b><b>).</b><br />
- комбинация предыдущих типов называется <b>смешанными ссылками</b><br> (например<b>: </b><b>F$7</b>).<br />
Тип ссылки можно задать след. способами:<br />
· с помощью клавиатуры<br />
· нажатием клавиши F4, пока не появиться необходимый тип ссылки<br />
Для обращения к группе ячеек используются специальные символы:<br />
<b>:</b> (двоеточие) – формирует обращение к блоку ячеек. Через двоеточие указывается левая верхняя и правая нижняя ячейки блока. Например<b>: </b><b>С4:D6</b> – обращение к ячейкам <b>С4, С5, С6, D4, D5, D6.</b><br />
<b>;</b> (точка с запятой) – обозначает объединение ячеек. Например, <b>D2:D4;D6:D8</b> – обращение к ячейкам <b>D2, D3, D4, D6, D7, D8.</b><br />
Для ввода формулы в ячейку следует ввести знак '=' и формулу для вычисления. После нажатия клавиши <b>Enter</b> в ячейке появится результат вычисления. При выделении ячейки, содержащей формулу, формула появляется в строке редактирования.<br />
<b>Функции</b><br />
<b>Функциями </b>в Microsoft Excel называют объединения нескольких вычислительных операций для решения определенной задачи. Функции в Microsoft Excel представляют собой формулы, которые имеют один или несколько аргументов. В качестве аргументов указываются числовые значения или адреса ячеек.<br />
Например:<br />
<b>=СУММ(А5:А9)</b><b> – </b>сумма ячеек<b> </b><b>А5, А6, А7, А8, А9;</b><br />
<b>=СРЗНАЧ(G4:G6)</b><b> – </b>среднее значение ячеек<b> </b><b>G4, G5, G6.</b><br />
Функции могут входить одна в другую, например:<br />
<b>=СУММ(F1:F20)ОКРУГЛ(СРЗНАЧ(strong:H8);2);</b><br />
Для введения функции в ячейку необходимо:<br />
· выделить ячейку для формулы;<br />
· вызывать <b>Мастер функций</b> с помощью команды <b>Функция</b> меню <b>Вставка</b>;<br />
· в диалоговом окне <b>Мастер функций</b> (рис.4), выбрать тип функции в поле <b>Категория,</b> затем функцию в списке <b>Функция</b>;<br />
· щелкнуть кнопку <b>ОК</b>;<br />
· в полях <b>Число1</b>, <b>Число2</b> и др. следующего окна ввести аргументы функции (числовые значения или ссылки на ячейки);<br />
· чтобы указать аргументы, находящуюся справа от поля, и выделить мышью ячейки, содержащие аргументы функции; для выхода из этого режима следует щелкнуть кнопку, которая находится под строкой формул;<br />
· щелкнуть <b>ОК</b>.<br />
