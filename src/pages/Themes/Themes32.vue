· в меню <b>Формат</b> выбрать команду <b>Строка</b> или <b>Столбец</b><b>,</b> затем выбрать команду <b>Отобразить</b><b>.</b><br />
 Чтобы показать строку, можно установить указатель мыши сразу под утолщенной границей заголовка строки (указатель мыши примет вид на рис.12 (1)) и перетянуть ее вниз. Чтобы показать столбец, следует установить указатель мыши вплотную справа от утолщенной границы заголовка<b> </b>столбца (указатель мыши будет иметь вид на рис.12 (2)) и перетянуть ее вправо.<br />
<b>6 Оформление таблиц</b><br />
Таблицы в Microsoft Excel можно обрамить рамкой и заполнить различными цветами. Для обрамления необходимо:<br />
· выделить ячейки, которые необходимо обрамить;<br />
· в меню <b>Формат</b> выбрать команду <b>Ячейки</b><b>;</b><br />
· выбрать вкладыш <b>Граница</b> (рис.13);<br />
· в поле <b>тип линии</b> выбрать тип линии рамки;<br />
· в списке <b>цвет</b> – цвет линии;<br />
· для обрамления выделенных ячеек извне следует щелкнуть кнопку <b>внешние</b>;<br />
· для обрамления внутренних границ ячеек следует щелкнуть кнопку <b>внутренние</b>;<br />
· для снятия обрамления выделенных ячеек следует щелкнуть кнопку <b>нет</b>;<br />
· с помощью группы кнопок <b>Отдельные</b> можно устанавливать и убирать отдельные линии; это также можно делать щелчком мыши в образце обрамления, представленного в окне;<br />
· щелкнуть <b>ОК</b><b>.</b><br />
Создавать рамки можно также с помощью скрытого списка <b>Границы</b>:<br />
· выделить ячейки, которые необходимо обрамить;<br />
· щелкнуть на стрелке рядом с кнопкой <b>Границы</b><b>;</b><br />
· выбрать тип обрамления в палитре рамок.<br />
Выбранный тип обрамления можно применить и для других ячеек, выделив эти ячейки и щелкнув на кнопке <b>Границы</b>. Для удобства использования палитру рамок можно вытянуть за заголовок с панели инструментов.<br />
Элементы таблицы можно заштриховать различным цветом и узорами:<br />
· выделить ячейки;<br />
· выбрать в меню<b> </b><b>Формат</b> команду <b>Ячейки</b><b>;</b><br />
· выбрать вкладыш <b>Вид</b><b>;</b><br />
· в палитре цветов выбрать цвет (в рамке <b>Образец</b> будет представлен образец с выбранными параметрами);<br />
· в списке<b> </b><b>Узор</b> выбрать узор;<br />
· щелкнуть кнопку <b>ОК</b>.<br />
Для оформления элементов таблицы различными цветами можно использовать панель инструментов <b>Форматирование</b>:<br />
· выделить нужные ячейки;<br />
