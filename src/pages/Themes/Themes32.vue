<b>Разделение окон</b><br />
Иногда необходимо одновременно просматривать различные части большой таблицы. Для этого следует разделить окно таблицы на подокна одним из следующих способов.<br />
· Установить указатель мыши на горизонтальную или вертикальную вешку разбиения (рис.14) (он примет вид двунаправленной стрелки) и перетянуть в нужное место. Размеры подокон можно изменять, перетягивая вешку разбиения.<br />
· Выделить столбец или строку, по которой следует разделить окно. Для разделения окна на 4 части необходимо выделить ячейку, по которой нужно выполнить разделение. Затем в меню <b>Окно</b> выбрать команду <b>Разделить</b><b>.</b> Окно разделится горизонтально по строке над выделенной ячейкой и вертикально по столбцу слева от нее.<br />
Чтобы отменить разделение окон, следует в меню <b>Окно</b> выбрать команду <b>Снять разделение</b> или дважды щелкнуть на вешке разбиения.<br />
<b>Создание нового окна</b><br />
С помощью команды <b>Новое</b> меню <b>Окно</b> можно создать дополнительное окно для активной рабочей книги, чтобы просматривать различные части рабочей книги одновременно. В этом случае в заголовке окна после имени рабочей книги через двоеточие указывается номер нового окна. Например, второе окно рабочей книги <b>Отчет</b> будет названо <b>Отчет:2.</b><br />
Упорядочить окна рабочей книги можно следующим способом. В меню <b>Окно</b><b> </b>выбрать команду <b>Расположить</b><b>. </b>В диалоговом окне <b>Расположение окон</b> (рис.15) выбрать подходящий вариант:<br />
<b>рядом</b> – окна будут занимать равные части экрана;<br />
<b>сверху вниз</b> – упорядочение горизонтальными полосами;<br />
<b>слева направо</b> – упорядочение вертикальными полосами;<br />
<b>каскадом</b> – упорядочение каскадом.<br />
Если установить флажок <b>только окна текущей книги</b>, то размещаться будут только окна активной рабочей книги.<br />
Для возврата к однооконному изображению необходимо развернуть активное окно на весь экран, щелкнув на кнопке восстановления окна.<br />
<b>Фиксация подокон</b><br />
Иногда необходимо зафиксировать одну часть таблицы. Например, зафиксировать заголовок таблицы, чтобы он всегда присутствовал на экране. Для фиксации только вертикальных или только горизонтальных заголовков следует:<br />
· чтобы зафиксировать горизонтальные заголовки, выделить строку ниже заголовков;<br />
· чтобы зафиксировать вертикальные заголовки, выделить столбец справа от заголовков;<br />
· в меню <b>Окно</b> выбрать команду <b>Закрепить области</b>.<br />
Для фиксации и вертикальных, и горизонтальных заголовков необходимо:<br />
· выделить ячейку, по которой следует зафиксировать заголовки (все строки выше выделенной ячейки и все столбцы слева от выделенной ячейки будут зафиксированы);<br />
· в меню <b>Окно</b> выбрать команду <b>Закрепить области</b>.<br />
Если разделить окно до фиксации подокон, они зафиксируются по их текущему месту, а не по активной ячейке.<br />
Отменяется фиксация окон и подокон командой <b>Снять закрепление областей</b><b> </b>меню<b> </b><b>Окно</b><b>.</b><br />
<ol>
	<li><b>Работа с базами данных</b></li>
</ol>
С помощью Microsoft Excel<b> </b>можно создавать и обрабатывать базы данных<b>. База данных</b> в Microsoft Excel – таблица, состоящая из однотипных <b>записей</b> (строк). Столбцы таблицы являются <b>полями записи</b> в базе данных. Под имена полей выделяется первая строка базы данных. Например, если базой данных считать телефонный справочник, то полями записи будут фамилии, номера телефонов и адреса абонентов.<br />
Для работы с базой данных необходимо сначала создать соответствующую таблицу. Если выделить ячейку в таблице и выбрать одну из команд обработки баз данных в меню <b>Данные</b><b>,</b> Microsoft Excel автоматически определяет и обрабатывает всю таблицу. <b>Данные</b>, расположенные в столбцах и строках рабочего листа, обрабатываются как набор полей, которые образуют записи.<br />
<b>Сортировка данных</b><br />
Сортировка позволяет переупорядочить строки в таблице по любому полю. Например, чтобы отсортировать данные по цене изделия. Для сортировки данных следует выделить одну ячейку таблицы и вызвать команду <b>Сортировка</b> меню <b>Данные</b><b>.</b><br />
В поле списка <b>Сортировать по</b> выбирается поле, по которому будут отсортированы данные, и тип сортировки:<br />
<b>по возрастанию</b> – цифры сортируются по возрастанию, текст – в алфавитном порядке, логические выражения – <b>ЛОЖЬ</b> предшествует <b>ИСТИНА</b><b>.</b><br />
<b>по убыванию</b> – сортировка в обратном порядке.<br />
В поле списка <b>Затем по</b> указывается поле, по которому будут отсортированы данные, имеющие одинаковые значения в первом ключевом поле. Во втором поле <b>Затем по</b> указывается поле, по которому будут отсортированы данные, имеющие одинаковые значения в первых двух ключевых полях.<br />
<b>Формы данных</b><br />
При выполнении характерных для баз данных операций, таких как поиск, сортировка, подведение итогов, Microsoft Excel автоматически рассматривает таблицу как базу данных.<br />
При просмотре, изменении, добавлении и удалении записи в базе данных, а также при поиске записей по определенному критерию удобно использовать <b>формы данных</b>. При обращении к команде <b>Форма</b> меню <b>Данные</b> Microsoft Excel считывает данные и создает диалоговое окно формы данных (рис.16). В форме данных на экран выводится одна запись. При вводе или изменении данных в полях этого окна изменяется содержимое соответствующих ячеек базы данных.<br />
Для использования форм данных таблица должна иметь имена столбцов. Имена столбцов становятся именами полей в форме данных. Поле соответствует каждому столбцу таблицы. Форма данных автоматически раскрывается так, чтобы вывести на экран сразу все поля в данной таблице, до 32 полей за один раз. С помощью полосы прокрутки можно прокручивать записи базы данных. Позиция выведенной записи указывается в верхнем правом углу. Передвигаться по полям формы можно с помощью мыши и клавиш <b>Tab</b> (вниз), <b>Shift+Tab</b> (вверх). В правой части окна расположены следующие кнопки.<br />
<b>Добавить</b> – очищает поля для ввода новой записи базы данных. Если снова щелкнуть кнопку Добавить<b>,</b> то введенные данные будут добавлены, как новая запись, в конец базы данных.<br />
<b>Удалить</b> – удаляет выведенную запись, другие записи базы данных сдвигаются. Удаленные записи не могут быть восстановлены.<br />
<b>Вернуть</b> – восстанавливает отредактированные поля в выведенной записи, удаляя сделанные изменения. Чтобы восстановить запись, необходимо сделать это перед нажатием клавиши <b>Enter</b> или перед переходом к другой записи.<br />
<b>Назад</b> – выводит предыдущую запись в списке. Если был определен критерий с помощью кнопки <b>Критерии</b><b>,</b> то кнопка <b>Назад</b> выведет предыдущую запись из тех, которые удовлетворяют заданному критерию.<br />
<b>Далее</b> – выводит следующую запись базы данных.<br />
<b>Критерии</b> – очищает поля перед вводом критериев сравнения с операторами сравнения для поиска необходимого подмножества записей.<br />
<b>Правка</b> – служит для выхода из режима ввода критериев. Доступна только после нажатия кнопки <b>Критерии</b><b>.</b><br />
<b>Очистить</b> – удаляет существующий критерий из окна диалога. Доступна только после нажатия кнопки <b>Критерии</b><b>.</b><br />
<b>Закрыть</b> – закрывает форму данных.<br />
Для добавления записи к базе данных необходимо:<br />
· выделить ячейку в таблице, к которой следует добавить запись;<br />
· в меню <b>Данные</b> выбрать команду <b>Форма</b><b>;</b><br />
· щелкнуть кнопку <b>Добавить</b><b>;</b><br />
· заполнить поля новой записи;<br />
· для перемещения к следующему полю нажать клавишу <b>Тab</b>;<br />
· после ввода данных нажать клавишу <b>Enter</b> для добавления записи;<br />
· после добавления всех необходимых записей, щелкнуть кнопку <b>Закрыть</b><b>.</b><br />
Новые записи будут добавлены в конец базы данных.<br />
<ol>
	<li><b>Создание диаграмм</b></li>
</ol>
<b>Диаграмма</b> – это представление данных таблицы в графическом виде, которое используется для анализа и сравнения данных. На диаграмме числовые данные ячеек изображаются в виде точек, линий, полос, столбиков, секторов и в другой форме. Группы элементов данных, отражающих содержимое ячеек одной строки или столбца на рабочем листе, составляют <b>ряд</b> <b>данных.</b><br />
Для создания диаграммы необходимо:<br />
· на рабочем листе выделить данные, по которым следует построить диаграмму, включая ячейки, содержащие имена категорий или рядов, которые будут использоваться в диаграмме;<br />
· выбрать команду <b>Диаграмма</b><b> </b>меню<b> </b><b>Вставка</b><br />
· в диалоговых окнах <b>Мастера</b> <b>диаграмм</b> следует выбрать тип, формат и другие параметры диаграммы;<br />
· для перехода к следующему шагу используется кнопка <b>Далее &gt;;</b><br />
· для построения диаграммы на любом шаге можно щелкнуть кнопку <b>Готово</b><b>,</b> тогда <b>Мастер диаграмм</b> самостоятельно закончит построение диаграммы;<br />
· в последнем (4-м) окне щелкнуть кнопку <b>Готово</b><b>.</b><br />
Диаграмму можно перетянуть мышью в любое место. Для изменения размера диаграммы необходимо щелкнуть на ней мышью и перетянуть маркеры размера. Для изменения типа и параметров построенной диаграммы следует щелкнуть на диаграмме правой клавишей мыши и в контекстном меню выбрать подходящую команду. Для удаления диаграммы следует щелкнуть на ней мышью, чтобы появились маркеры размера, и нажать клавишу <b>Delete</b>.<br />
