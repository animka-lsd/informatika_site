<b>Массивы формул</b><br />
Массивы формул удобно использовать для введения однотипных формул и обработки данных в виде таблиц. Например, для вычисления модуля от чисел, размещенных в ячейках <b>B1, C1, D1, E1</b>, вместо ввода формул в каждую ячейку можно ввести одну формулу – массив для всех ячеек. Microsoft Excel добавляет вокруг массива формул фигурные скобки { }, по которым его можно отличить.<br />
Для создания массива формул необходимо:<br />
· выделить ячейки, в которых должен находиться массив формул (рис.5);<br />
· ввести формулу обычным способом, указав в качестве аргументов группу ячеек-аргументов;<br />
· в последнем окне вместо кнопки <b>ОК</b> нажать комбинацию клавиш <b>Ctrl+Shift+Enter.</b><b> </b><br />
Для редактирования массива формул необходимо:<br />
· выделить ячейки, в которых находится массив;<br />
· щелкнуть мышью внутри строки редактирования и отредактировать формулу;<br />
· нажать комбинацию клавиш <b>Ctrl+Shift+Enter.</b><br />
