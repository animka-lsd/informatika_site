· для того, чтобы Microsoft Excel учитывал при поиске регистр символов (нижний – строчная буква, верхний – прописная), введенных в поле <b>Что</b>, следует установить флажок <b>Учитывать регистр</b><b>;</b><br />
· для того, чтобы Microsoft Excel искал только те ячейки, все содержимое которых отвечает шаблону поиска, следует установить флажок <b>Ячейка целиком;</b><br />
· в поле <b>Просматривать</b> необходимо выбрать направление поиска <b>по срокам</b> или <b>по столбцам</b>;<br />
· в поле <b>Область поиска</b> следует выбрать <b>формулы</b><b>,</b> <b>значения</b> или <b>примечания</b> (поиск будет вестись только в указанных компонентах ячеек);<br />
· щелкнуть кнопку <b>Найти далее</b> (после чего будет выделено первое слово, отвечающее условиям поиска)<b>;</b><br />
· для поиска следующего фрагмента, отвечающего условиям, повторно щелкнуть кнопку <b>Найти далее</b>;<br />
· для замены выделенных символов необходимо щелкнуть кнопку<b> </b><b>Заменить</b><b>,</b> после чего появится диалоговое окно <b>Заменить</b><b>. </b><br />
