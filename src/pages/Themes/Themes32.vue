<b>Удаление элементов таблицы</b><br />
Для удаления элементов таблицы следует выделить их и в меню <b>Правка</b> выбрать команду <b>Удалить</b><b>. </b>Столбцы и строки удаляются без запросов, а при удалении ячеек появляется диалоговое окно, в котором необходимо выбрать вариант удаления элемента:<br />
