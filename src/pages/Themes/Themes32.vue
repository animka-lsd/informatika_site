<b>строку</b> – удалить строку;<br />
<b>столбец</b> – удалить столбец.<br />
<b>Удаление содержимого элементов таблицы</b><br />
Для удаления содержимого ячеек без удаления их самих следует:<br />
· выделить группу ячеек, которые необходимо очистить;<br />
· выбрать пункт <b>Очистить</b> из меню <b>Правка</b>, а затем один из пунктов:<br />
<b>Все</b><b> – </b>очистить все;<br />
<b>Форматы</b> – устанавливается формат ячеек по умолчанию;<br />
<b>Содержимое</b> – очистка только содержимого ячеек;<br />
<b>Примечание</b> – очистка ячеек от примечаний.<br />
· щелкнуть <b>ОК</b>.<br />
Для удаления только содержимого ячеек также можно выделить их и нажать клавишу <b>Delete</b>.<br />
<b>Копирование и перемещение данных</b><br />
Для перемещения и копирования содержимого ячеек используется буфер промежуточного хранения информации (<b>Clipboard</b>)<b>,</b> позволяющий перемещать данные как в пределах таблицы, так и в другие Windows-программы.<br />
Для перемещения содержимого ячеек необходимо:<br />
· выделить ячейки, содержимое которых необходимо переместить;<br />
· вызвать команду <b>Вырезать</b> меню <b>Правка</b>;<br />
· выделить левую верхнюю ячейку области, в которую необходимо вставить фрагмент;<br />
· вызвать команду <b>Вставить</b> меню <b>Правка</b>.<br />
Фрагмент из буфера будет вставлен в указанное место.<br />
При перемещении ячеек, содержащих формулы, координаты ячеек аргументов не изменяются. Например, при перемещении формулы <b>=А1+А2</b> из ячейки <b>А3</b> в ячейку <b>В3</b> формула останется <b>=А1+А2.</b><br />
Щелчок правой клавишей мыши на любом объекте таблицы вызывает контекстное меню, содержащее команды для обработки данного объекта (рис.7).<br />
