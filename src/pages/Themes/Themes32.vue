<b>Числовые форматы</b><br />
<b> </b><br />
При создании нового рабочего листа все ячейки имеют одинаковый формат. Он называется <b>общий. </b>Числа записываются в обычной или экспоненциальной форме и выравниваются по правому краю ячейки. Текст выравнивается по левому краю. Логические значения и значения ошибок выравниваются по центру ячеек.<br />
<b>Все форматы в программе </b><b>Excel</b><b> разбиваются на следующие:</b><br />
· общий;<br />
· числовой; денежный,;<br />
· финансовый;<br />
· дата;<br />
· время;<br />
· процентный;<br />
· экспоненциальный;<br />
· текстовый;<br />
· дополнительный.<br />
