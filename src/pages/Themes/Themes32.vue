Рабочий лист представляет собой таблицу, состоящую из столбцов и строк. Столбцы именуются латинскими буквами, а строки – цифрами. Каждая ячейка таблицы имеет <b>адрес</b>, который состоит из имени строки и имени столбца. Например, если ячейка находится в столбце <b>F</b> и строке <b>7</b>, то она имеет адрес <b>F7</b>.<br />
<b>Выделение элементов таблицы</b><br />
Одна из ячеек таблицы всегда является <b>активной.</b> Активная ячейка выделяется рамкой. Чтобы сделать ячейку активной, необходимо клавишами управления курсором подвести рамку к этой ячейке или щелкнуть в ней мышью.<br />
Для выделения нескольких смежных ячеек необходимо установить указатель мыши в одну из ячеек, нажать левую кнопку мыши и, не отпуская ее, растянуть выделение на всю область. Для выделения нескольких несмежных групп ячеек следует выделить одну группу, нажать клавишу <b>Ctrl</b> и, не отпуская ее, выделить другие ячейки.<br />
Чтобы выделить целый столбец или строку таблицы, необходимо щелкнуть мышью на его имени. Для выделения нескольких столбцов или строк следует щелкнуть на имени первого столбца или строки и растянуть выделение на всю область.<br />
Для выделения нескольких листов необходимо нажать клавишу <b>Ctrl</b> и, не отпуская ее, щелкать на ярлыках листов.<br />
<b>Заполнение ячеек</b><br />
Для ввода данных в ячейку необходимо сделать ее активной и ввести данные с клавиатуры. Данные появятся в ячейке и в строке редактирования (рис.2). Для завершения ввода следует нажать <b>Enter</b> или одну из клавиш управления курсором. Процесс ввода данных закончится и активной станет соседняя ячейка.<br />
