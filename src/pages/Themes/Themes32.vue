<b>Сообщения об ошибках</b><br />
Если формула в ячейке не может быть правильно вычислена, Microsoft Excel выводит в ячейку сообщение об ошибке. Если формула содержит ссылку на ячейку, которая содержит значения ошибки, то вместо этой формулы также будет выводиться сообщение об ошибке. Значение сообщений об ошибках следующее:<br />
<b>####</b> – ширина ячейки не позволяет отобразить число в заданном формате;<br />
<b>#ИМЯ?</b> – Microsoft Excel не смог распознать имя, использованное в формуле;<br />
<b>#ДЕЛ/0!</b> – в формуле делается попытка деления на нуль;<br />
<b>#ЧИСЛО!</b> – нарушены правила задания операторов, принятые в математике;<br />
<b>#Н/Д</b> – такое сообщение может появиться, если в качестве аргумента задана ссылка на пустую ячейку;<br />
<b>#ПУСТО!</b> – неверно указано пересечение двух областей, которые не имеют общих ячеек;<br />
<b>#ССЫЛКА!</b> – в формуле задана ссылка на несуществующую ячейку;<br />
<b>#ЗНАЧ!</b> – использован недопустимый тип аргумента.<br />
