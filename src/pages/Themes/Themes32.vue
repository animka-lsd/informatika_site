<b>Замена данных</b><br />
Команда <b>Заменить</b> меню<b> </b><b>Правка</b> аналогична команде <b>Найти</b><b>,</b> но она позволяет найденные данные заменять на другие. В поле <b>Что</b> следует указать, что нужно искать, а в поле <b>Заменить на</b> указать, на что необходимо заменить найденный фрагмент. После нажатия кнопки <b>Найти</b> <b>далее</b> будет выделено первое слово, отвечающее условиям поиска. Для его замены следует щелкнуть кнопку <b>Заменить</b>.<br />
<b>Операции с листами рабочих книг</b><br />
Для переименования листа необходимо выполнить двойной щелчок на его ярлыке и ввести новое имя. Его длина не должна превышать 31 символ, и не может содержать следующие символы: \ / ? * : [ ] ).<br />
Для копирования или перемещения листов рабочей книги необходимо:<br />
· открыть рабочую книгу-источник, листы которой необходимо скопировать, и рабочую книга-приемник;<br />
· выбрать лист, который нужно скопировать;<br />
· выбрать команду <b>Переместить/ скопировать лист</b> меню <b>Правка</b>;<br />
· в поле <b>в книгу</b> (рис.9) следует выбрать рабочую книгу-приемник (для того, чтобы переместить листы в середине рабочей книги, необходимо выбрать имя текущей рабочей книги);<br />
· в поле <b>Перед листом</b> следует выбрать лист, перед которым необходимо вставить перемещаемый лист;<br />
· для копирования листа необходимо установить флажок <b>Создавать копию</b>;<br />
· щелкнуть кнопку <b>ОК</b><b>.</b><br />
Для перемещения листа в пределах одной рабочей книги можно перетянуть его ярлык на ярлык того листа, перед которым его необходимо вставить. Если при этом нажимать клавишу <b>Ctrl</b>, то лист будет скопирован.<br />
Для удаления листа необходимо сделать его активным и в меню <b>Правка</b> выбрать команду <b>Удалить лист</b><b>. </b><br />
Для вставки листа следует сделать активным лист, на месте которого будет вставлен новый, и выбрать команду <b>Лист</b> меню <b>Вставка</b><b>.</b><br />
