<b>Двойное, по ячейке – </b>подчеркивание двойной линией по ширине ячейки;<br />
· в поле <b>Цвет – </b>цвет символов;<br />
· в рамке <b>Эффекты </b>можно установить флажки:<br />
<b>зачеркнутый – </b>зачеркивание текста одинарной линией;<br />
<b>верхний индекс – </b>размер символов уменьшается, текст располагается выше;<br />
<b>нижний индекс – </b>размер символов уменьшается, текст располагается ниже;<br />
· если установить флажок <b>Обычный</b>, то в ячейке установится шрифт по умолчанию;<br />
· щелкнуть<b> </b><b>ОК</b><b>. </b><br />
Для быстрого форматирования символов используется панель инструментов <b>Форматирование</b>.<br />
